
import 'package:brain_rise/Screens/onboarding/select_subject_screen.dart';
import 'package:brain_rise/providers/user_provider.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

class SelectExamTypeScreen extends StatefulWidget {
  final bool isAddingNewExam;

  const SelectExamTypeScreen({super.key, this.isAddingNewExam = false});

  @override
  State<SelectExamTypeScreen> createState() => _SelectExamTypeScreenState();
}

class _SelectExamTypeScreenState extends State<SelectExamTypeScreen> {
  String? selectedExam;
  bool _isLoading = false;

  final List<Map<String, String>> exams = [
    {
      'id': 'WAEC',
      'name': 'WAEC',
      'description': 'West African Examinations Council',
    },
    {
      'id': 'NECO',
      'name': 'NECO',
      'description': 'National Examinations Council',
    },
    {
      'id': 'IELTS',
      'name': 'IELTS',
      'description': 'International English Language Testing System',
    },
    {
      'id': 'JAMB',
      'name': 'JAMB',
      'description': 'Joint Admissions and Matriculation Board',
    },
  ];

  List<Map<String, String>> get availableExams {
    if (widget.isAddingNewExam) {
      final userProvider = context.read<UserProvider>();
      final existingExams =
          userProvider.examPreparations.map((e) => e.examType).toSet();
      return exams
          .where((exam) => !existingExams.contains(exam['id']))
          .toList();
    }
    return exams;
  }

  void _navigateToSubjects() async {
    if (selectedExam == null) return;

    setState(() => _isLoading = true);

    if (widget.isAddingNewExam) {
      // Navigate to select subjects and add new exam preparation
      final result = await Navigator.of(context).push<Map<String, dynamic>>(
        MaterialPageRoute(
          builder: (_) => SelectSubjectScreen(
            examType: selectedExam!,
            isAddingNewExam: true,
          ),
        ),
      );

      if (result != null && mounted) {
        final userProvider = context.read<UserProvider>();
        await userProvider.addExamPreparation(
          examType: result['examType'] as String,
          selectedSubjects: result['selectedSubjects'] as List<String>,
        );

        if (mounted) {
          Navigator.pop(context);
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('Added ${result['examType']} preparation!'),
            ),
          );
        }
      } else if (mounted) {
        setState(() => _isLoading = false);
      }
    } else {
      // Original flow - first time user
      Navigator.of(context).push(
        MaterialPageRoute(
          builder: (_) => SelectSubjectScreen(examType: selectedExam!),
        ),
      );
      if (mounted) {
        setState(() => _isLoading = false);
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final displayExams = availableExams;

    return Scaffold(
      appBar: AppBar(
        title: Text(
          widget.isAddingNewExam ? 'Add New Exam' : 'Select Your Target Exam',
        ),
      ),
      body: SafeArea(
        child: Padding(
          padding: const EdgeInsets.all(24.0),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                widget.isAddingNewExam
                    ? 'Which exam would you like to add?'
                    : 'What exam are you preparing for?',
                style: Theme.of(context).textTheme.displaySmall,
              ),
              const SizedBox(height: 8),
              Text(
                'We\'ll customize your learning path',
                style: Theme.of(context).textTheme.bodyMedium,
              ),
              const SizedBox(height: 32),
              Expanded(
                child: displayExams.isEmpty
                    ? Center(
                        child: Column(
                          mainAxisAlignment: MainAxisAlignment.center,
                          children: [
                            Icon(
                              Icons.check_circle,
                              size: 64,
                              color: Colors.green[400],
                            ),
                            const SizedBox(height: 16),
                            Text(
                              'You\'re already preparing for all exams!',
                              style: Theme.of(context).textTheme.titleLarge,
                              textAlign: TextAlign.center,
                            ),
                          ],
                        ),
                      )
                    : ListView.builder(
                        itemCount: displayExams.length,
                        itemBuilder: (context, index) {
                          final exam = displayExams[index];
                          final isSelected = selectedExam == exam['id'];

                          return Padding(
                            padding: const EdgeInsets.only(bottom: 12.0),
                            child: InkWell(
                              onTap: () {
                                setState(() {
                                  selectedExam = exam['id'];
                                });
                              },
                              borderRadius: BorderRadius.circular(16),
                              child: Container(
                                padding: const EdgeInsets.all(20),
                                decoration: BoxDecoration(
                                  color: isSelected
                                      ? Theme.of(context)
                                          .primaryColor
                                          .withOpacity(0.1)
                                      : Theme.of(context).cardTheme.color,
                                  borderRadius: BorderRadius.circular(16),
                                  border: Border.all(
                                    color: isSelected
                                        ? Theme.of(context).primaryColor
                                        : Colors.grey.shade300,
                                    width: 2,
                                  ),
                                ),
                                child: Row(
                                  children: [
                                    Container(
                                      width: 48,
                                      height: 48,
                                      decoration: BoxDecoration(
                                        color: isSelected
                                            ? Theme.of(context).primaryColor
                                            : Colors.grey.shade200,
                                        shape: BoxShape.circle,
                                      ),
                                      child: Center(
                                        child: Text(
                                          exam['name']![0],
                                          style: TextStyle(
                                            fontSize: 24,
                                            fontWeight: FontWeight.bold,
                                            color: isSelected
                                                ? Colors.white
                                                : Colors.grey,
                                          ),
                                        ),
                                      ),
                                    ),
                                    const SizedBox(width: 16),
                                    Expanded(
                                      child: Column(
                                        crossAxisAlignment:
                                            CrossAxisAlignment.start,
                                        children: [
                                          Text(
                                            exam['name']!,
                                            style: Theme.of(context)
                                                .textTheme
                                                .headlineMedium,
                                          ),
                                          Text(
                                            exam['description']!,
                                            style: Theme.of(context)
                                                .textTheme
                                                .bodySmall,
                                          ),
                                        ],
                                      ),
                                    ),
                                    if (isSelected)
                                      Icon(
                                        Icons.check_circle,
                                        color: Theme.of(context).primaryColor,
                                      ),
                                  ],
                                ),
                              ),
                            ),
                          );
                        },
                      ),
              ),
              SizedBox(
                width: double.infinity,
                child: ElevatedButton(
                  onPressed: selectedExam == null ||
                          _isLoading ||
                          displayExams.isEmpty
                      ? null
                      : _navigateToSubjects,
                  child: _isLoading
                      ? const SizedBox(
                          height: 20,
                          width: 20,
                          child: CircularProgressIndicator(strokeWidth: 2),
                        )
                      : Text(widget.isAddingNewExam ? 'Add Exam' : 'Continue'),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }
}


